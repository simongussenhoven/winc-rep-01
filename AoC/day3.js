const getTrees = (map, step) => {
    let trees = 0;
    let position = 0;
    map.forEach(row => {
        let newRow = row.repeat(Math.ceil((position + 1) / (row.length)));
        if (newRow[position] == "#") {
            trees++;
        }
        position += step;
    })
    return trees
}

const getTrees2 = (map, step) => {
    let trees = 0;
    let position = 0;
    let rowCount = 0;
    map.forEach(row => {
        if (rowCount % 2 == 0) {
            rowCount++
            let newRow = row.repeat(Math.ceil((position + 1) / (row.length)));
            if (newRow[position] == "#") {
                trees++;
            }
            position += step;
        }
        else {
            rowCount++
        }
    })
    return trees
}


